def get_chrimsonR_irradiance_data(): #fix later asw
    """
    Returns ChrimsonR dose–response arrays for 1P and 2P excitation.

    1P (590 nm) from Klapoetke et al. 2014, Fig.4C:
      irr_1p [mW/mm²] → I_1p [nA]
    2P (920 nm) approximated by scaling ChR2 values (Rickgauer & Tank 2009)
      by σ2(ChrimsonR)/σ2(ChR2) ≈ 0.23.
    """
    # — One-photon (590 nm) —
    irr_1p = np.array([0.10, 0.50, 1.00, 2.30, 4.60])       # mW/mm²
    I_1p   = np.array([0.18, 1.20, 2.50, 4.00, 5.50])       # nA  [oai_citation:0‡PMC]https://pmc.ncbi.nlm.nih.gov/articles/PMC3943671/

    # — Two-photon (920 nm) — same laser powers as for ChR2:
    power_2p = np.array([5.0, 10.0, 20.0, 40.0, 80.0])      # mW total
    # ChR2 I_2p (nA) from Rickgauer & Tank 2009
    chr2_I2p  = np.array([0.0105, 0.0234, 0.0414, 0.0969, 0.1979])
    # scale by σ₂(ChrimsonR)/σ₂(ChR2) ≈ 0.23 (Shemesh et al. 2017)
    I_2p      = chr2_I2p * 0.23                           # nA

    # convert 2P power → irradiance over 10 μm radius
    irr_2p = power_2p / (np.pi * 0.01**2)     # mW/mm²

    return {
        "irr_1p": irr_1p,
        "I_1p":   I_1p,
        "irr_2p": irr_2p,
        "I_2p":   I_2p,
    }